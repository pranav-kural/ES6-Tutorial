/* Calculator Functions */

// Add two numbers
export const add = (x, y) => x + y;

// Substract two numbers
export const substract = (x, y) => x - y;

// Multiply two numbers
multiply = (x, y) => x * y;

// Another syntax to export function
export const multi = multiply;

// Divide two numbers
divide = (x, y) => x / y;

// Yet another syntax option for exporting a function excplicitly
export { divide };

// Print numbers to console
export default log = (x, y) => console.log("First number: " + x + ", Second number: " + y);