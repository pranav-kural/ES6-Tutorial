/* Calculator Functions */

// Add two numbers
add = (x, y) => x + y;

// Substract two numbers
substract = (x, y) => x - y;

// Multiply two numbers
multiply = (x, y) => x * y;

// Divide two numbers
divide = (x, y) => x / y;

// Print numbers to console
log = (x, y) => console.log("First number: " + x + ", Second number: " + y);