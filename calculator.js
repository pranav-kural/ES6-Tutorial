/* Calculator functions */

// Add two numbers
function add(x, y) {
  return x + y;
}


// Substract two numbers
function substract(x, y) {
  return x - y;
}

// Multiply two numbers
function multiply(x, y) {
  return x * y;
}


// Divide two numbers
function divide(x, y) {
  return x / y;
}

// Print numbers to console
function log(x, y) {
  console.log("First number: " + x + ", Second number: " + y);
}
